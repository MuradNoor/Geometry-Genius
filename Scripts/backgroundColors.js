// change div color
    // triangle div
    const triangleDiv = document.getElementById("triangle");

    triangleDiv.addEventListener("mouseenter", function() {
        triangleDiv.style.backgroundColor = "#D7CCC8"; // Change color on hover
    });

    triangleDiv.addEventListener("mouseleave", function() {
        triangleDiv.style.backgroundColor = "white"; // Revert color when not hovering
    });

